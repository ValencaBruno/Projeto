    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"","Curso","Tipo de Curso","Modalidade"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("ID_CURSOS"),conex.rs.getString("CURSO"),conex.rs.getString("TIPO_DE_CURSO"),conex.rs.getString("MODALIDADE")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayList"+ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        //ModeloTabela modelo = new ModeloTabela(dados, colunas);
        //jTableCurso.setModel((TableModel) modelo);
        jTableCurso.setModel(modelo);
        jTableCurso.getColumnModel().getColumn(0).setMinWidth(0);
        jTableCurso.getColumnModel().getColumn(0).setMaxWidth(0);
        jTableCurso.getColumnModel().getColumn(0).setResizable(false);
        jTableCurso.getColumnModel().getColumn(1).setPreferredWidth(262);
        jTableCurso.getColumnModel().getColumn(1).setResizable(false);
        jTableCurso.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableCurso.getColumnModel().getColumn(2).setResizable(false);
        jTableCurso.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableCurso.getColumnModel().getColumn(3).setResizable(false);
        jTableCurso.getTableHeader().setReorderingAllowed(false);
        jTableCurso.setAutoResizeMode(jTableCurso.AUTO_RESIZE_OFF);
        jTableCurso.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
        
    }
    
    
