    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        String sql = "SELECT ID_PROFESSOR, MATRICULA, PROFESSOR, CPF FROM Professor";
        String matricula = jTextFieldMatricula.getText();
        String professor = jTextFieldNome.getText();
        String cpf = jTextFieldCPF.getText();
        
        int x = 0;
        
        if ((matricula.equals("")) && (professor.equals("")) && (cpf.equals(""))){
        }else{
            sql = sql + " WHERE ";
        }
        
        if (matricula.equals("")){
        }else{
            sql = sql + " MATRICULA LIKE '%" + matricula + "%'";
            x = 1;
        }
        
        if (professor.equals("")){
        }else{
            if(x==1){
                sql = sql + " AND";
            }
            sql = sql + " PROFESSOR LIKE '%" + professor + "%'";
            x = 1;
        }
        
        if (cpf.equals("")){
        }else{
            if(x==1){
                sql = sql + " AND";
            }
            sql = sql + " CPF LIKE '%" + cpf + "%'";
        }
        
        sql = sql + " ORDER BY PROFESSOR;";
        //JOptionPane.showMessageDialog(null, sql);
        
        preencherTabela(sql);
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

