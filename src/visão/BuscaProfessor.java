    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"","Matrícula","Nome","CPF"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("ID_PROFESSOR"),conex.rs.getString("MATRICULA"),conex.rs.getString("PROFESSOR"),conex.rs.getString("CPF")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher ArrayList"+ex);
        }
        
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        //ModeloTabela modelo = new ModeloTabela(dados, colunas);
        //jTableCurso.setModel((TableModel) modelo);
        jTableProfessor.setModel(modelo);
        jTableProfessor.getColumnModel().getColumn(0).setMinWidth(0);
        jTableProfessor.getColumnModel().getColumn(0).setMaxWidth(0);
        jTableProfessor.getColumnModel().getColumn(0).setResizable(false);
        jTableProfessor.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableProfessor.getColumnModel().getColumn(1).setResizable(false);
        jTableProfessor.getColumnModel().getColumn(2).setPreferredWidth(262);
        jTableProfessor.getColumnModel().getColumn(2).setResizable(false);
        jTableProfessor.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableProfessor.getColumnModel().getColumn(3).setResizable(false);
        jTableProfessor.getTableHeader().setReorderingAllowed(false);
        jTableProfessor.setAutoResizeMode(jTableProfessor.AUTO_RESIZE_OFF);
        jTableProfessor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
        
    }
    
    
