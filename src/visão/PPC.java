    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String caixa = (String) jComboBoxPPC.getSelectedItem();
        
        if(caixa.equals("Selecione o Curso...")){
        }else{
            //JOptionPane.showMessageDialog(null, "aqui");
            mod.setPERFIL_CURSO(jTextFieldPerfilCurso.getText());
            mod.setREPRESENTACAO_GRAFICA(jTextFieldRepreGrafica.getText());
            mod.setFORMA_DE_ACESSO_CURSO(jTextFieldAcessoCurso.getText());
            mod.setSISTEMA_DE_AVALIACAO(jTextFieldSistemaAvaliacao.getText());
            mod.setSISTEMA_DE_AVALIACAO_DO_PROCESSO_ENSINO_APRENDIZAGEM(jTextFieldSistemaEnsinoAprendizagem.getText());
            mod.setTRABALHO_DE_CONCLUSAO_DE_CURSO(jTextFieldTCC.getText());
            mod.setESTAGIO_CURRICULAR(jTextFieldEstagioCurricular.getText());
            mod.setPOLITICA_DE_ATENDIMENTO_A_PESSOA_COM_DEFICIENCIA(jTextFieldPoliticaPCD.getText());
            control.Salvar(mod, caixa);

            jTextFieldPerfilCurso.setEnabled(false);
            jTextFieldRepreGrafica.setEnabled(false);
            jTextFieldAcessoCurso.setEnabled(false);
            jTextFieldSistemaAvaliacao.setEnabled(false);
            jTextFieldSistemaEnsinoAprendizagem.setEnabled(false);
            jTextFieldTCC.setEnabled(false);
            jTextFieldEstagioCurricular.setEnabled(false);
            jTextFieldPoliticaPCD.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonVoltar.setEnabled(true);
            jButtonAlterar.setEnabled(true);
            jComboBoxPPC.setEnabled(true);
            jButtonBuscar.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

