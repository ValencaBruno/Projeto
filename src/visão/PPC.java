    public void preencheCurso(){
        conex.conexao();
        conex.executaSql("SELECT CURSO FROM Cursos ORDER BY CURSO;");
        try {
            conex.rs.first();
            //jComboBoxPPC.removeAllItems();
            //jComboBoxPPC.addItem("Selecione o Curso...");
            do{
                jComboBoxPPC.addItem(conex.rs.getString("CURSO"));
            }while(conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher cursos "+ex);
        }
        conex.desconecta();
    }
    
