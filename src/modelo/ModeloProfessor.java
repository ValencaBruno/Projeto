public class ModeloProfessor {
    private String matricula;
    private String professor;
    private String cpf;
    private String maior_titulacao;
    private String area;
    private String curriculo;
    private String data;
    private int ID_PROFESSOR;

    /**
     * @return the matricula
     */
    public String getMatricula() {
        return matricula;
    }

    /**
     * @param matricula the matricula to set
     */
    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    /**
     * @return the professor
     */
    public String getProfessor() {
        return professor;
    }

    /**
     * @param professor the professor to set
     */
    public void setProfessor(String professor) {
        this.professor = professor;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the maior_titulacao
     */
    public String getMaior_titulacao() {
        return maior_titulacao;
    }

    /**
     * @param maior_titulacao the maior_titulacao to set
     */
    public void setMaior_titulacao(String maior_titulacao) {
        this.maior_titulacao = maior_titulacao;
    }

    /**
     * @return the area
     */
    public String getArea() {
        return area;
    }

    /**
     * @param area the area to set
     */
    public void setArea(String area) {
        this.area = area;
    }

    /**
     * @return the curriculo
     */
    public String getCurriculo() {
        return curriculo;
    }

    /**
     * @param curriculo the curriculo to set
     */
    public void setCurriculo(String curriculo) {
        this.curriculo = curriculo;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the ID_PROFESSOR
     */
    public int getID_PROFESSOR() {
        return ID_PROFESSOR;
    }

    /**
     * @param ID_PROFESSOR the ID_PROFESSOR to set
     */
    public void setID_PROFESSOR(int ID_PROFESSOR) {
        this.ID_PROFESSOR = ID_PROFESSOR;
    }
    
    
}
